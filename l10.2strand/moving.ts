namespace strand3 {
    export class Moves {
        position: Vector;
        velocity: Vector;

        constructor(_position: Vector, _velocity: Vector) {
            this.position = _position;
            this.velocity = _velocity;

        }
        draw(): void {
            this.draw();
        }
        move(_timeslice: number): void {
            this.move(_timeslice);
        }
    }
}